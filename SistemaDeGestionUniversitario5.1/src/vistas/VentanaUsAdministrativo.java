    public void informacionAdministrativo(Administrativo admin) {
        Administrativo aux = admin;
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(aux.getBirthDate());
        lblNombreAdministrativo.setText(aux.getNombre());
        lblBirthDateAdmin.setText(String.valueOf(calendar.get(Calendar.YEAR)));
        lblEdadAdmin.setText(String.valueOf(2024 - calendar.get(Calendar.YEAR)));

    }
