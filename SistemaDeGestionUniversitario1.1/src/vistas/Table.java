        private void llenarTabla() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"Nombre", "Id", "BirthDate", "Contraseña"}, controladorPrincipal.getEsts().size());
        table.setModel(modelo);

        TableModel modeloDocentes = table.getModel();
        for (int i = 0; i < controladorPrincipal.getEsts().size(); i++) {
            Estudiante estudiante = controladorPrincipal.getEsts().get(i);
            modeloDocentes.setValueAt(estudiante.getNombre(), i, 0);
            modeloDocentes.setValueAt(estudiante.getId(), i, 1);
            modeloDocentes.setValueAt(estudiante.getBirthDate(), i, 2);
            modeloDocentes.setValueAt(estudiante.getContraseña(), i, 3);
        }
    }

   
