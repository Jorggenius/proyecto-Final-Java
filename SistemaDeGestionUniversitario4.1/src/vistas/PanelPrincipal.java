    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String usuario = txtUsuario.getText();
        String contraseña = txtContraseña.getText();
        if ("".equals(usuario) && "".equals(contraseña)) {
            PanelAdministrador panelAdministrador = new PanelAdministrador();
            panelAdministrador.setVisible(true);
            this.dispose();
        } else {
            Usuario aux = controlP.validarUsuario(usuario, contraseña);
            if (aux != null) {
                JOptionPane.showMessageDialog(null, "ACCESO PERMITIDO");
                if (aux instanceof Estudiante) {
                    if (aux.isCambioContraseña()) {
                        VentanaUsEstudiante ventanaE = new VentanaUsEstudiante();
                        ventanaE.setVisible(true);
                        ventanaE.informacionEstudiante((Estudiante) aux);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "No ha cambiado la contraseña");
                        panelCambiarContraseña panelCC = new panelCambiarContraseña();
                        panelCC.setVisible(true);
                        panelCC.cambiarContraseña(aux);
                        this.dispose();
                    }

                } else if (aux instanceof Docente) {
                    if (aux.isCambioContraseña()) {
                        VentanaUsDocente ventanaD = new VentanaUsDocente((Docente) aux);
                        ventanaD.setVisible(true);
                        ventanaD.informacionDocente((Docente) aux);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "No ha cambiado la contraseña");
                        panelCambiarContraseña panelCC = new panelCambiarContraseña();
                        panelCC.setVisible(true);
                        panelCC.cambiarContraseña(aux);
                        this.dispose();
                    }
                } else if (aux instanceof Administrativo) {
                    if (aux.isCambioContraseña()) {
                        VentanaUsAdministrativo ventanaA = new VentanaUsAdministrativo();
                        ventanaA.setVisible(true);
                        ventanaA.informacionAdministrativo((Administrativo) aux);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "No ha cambiado la contraseña");
                        panelCambiarContraseña panelCC = new panelCambiarContraseña();
                        panelCC.setVisible(true);
                        panelCC.cambiarContraseña(aux);
                        this.dispose();
                    }
                } else if (aux instanceof AdminLab) {
                    VentanaUsAdminLab ventanaAL = new VentanaUsAdminLab();
                    ventanaAL.setVisible(true);
                    ventanaAL.informacionAdminLab((AdminLab) aux);
                    this.dispose();

                }
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "ACCESO DENEGADO");
                limpiarCampos();
            }
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

    public void limpiarCampos() {
        txtUsuario.setText("");
        txtContraseña.setText("");
    }

