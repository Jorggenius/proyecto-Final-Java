    public void llenarTablaEstudiante() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"Nombre", "Id", "Programa"}, curso.getEstudiantes().size());
        tablaEstudiantes.setModel(modelo);
        TableModel modeloEstudiante = tablaEstudiantes.getModel();
        for (int i = 0; i < curso.getEstudiantes().size(); i++) {
            Estudiante estudiante = curso.getEstudiantes().get(i);
            modeloEstudiante.setValueAt(estudiante.getNombre(), i, 0);
            modeloEstudiante.setValueAt(estudiante.getId(), i, 1);
            modeloEstudiante.setValueAt(estudiante.getPrograma(), i, 2);
        }
    }

    public void llenarComboBoxEstudiante() {
        ArrayList<Estudiante> ests = controlVAE.listaUsuarioEsts();
        DefaultComboBoxModel<String> modeloEsts = new DefaultComboBoxModel<>();
        for (Estudiante estudiante : ests) {
            modeloEsts.addElement(estudiante.getNombre());
        }
        jComboEstudiantes.setModel(modeloEsts);
    }

