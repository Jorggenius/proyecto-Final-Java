    private void btnInscribirEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirEstudianteActionPerformed
        try {
            int indexE = jComboEstudiantes.getSelectedIndex();
            if (controlVAE.validarHorariosEstudiante(indexE, idCurso)) {
                controlVAE.inscribirEstudiante(indexE, curso.getId());
                llenarTablaEstudiante();
            } else {
                JOptionPane.showMessageDialog(null, "Imposible inscribir \n se crusan los horarios");
            }
        } catch (EstudianteNoInscrito ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnInscribirEstudianteActionPerformed

