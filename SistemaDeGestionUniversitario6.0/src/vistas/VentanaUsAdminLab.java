    public void informacionAdminLab(AdminLab adminLab) {
        AdminLab aux = adminLab;
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(aux.getBirthDate());
        lblNombreAdminLabs.setText(aux.getNombre());
        lblBirthDateAdminLabs.setText(String.valueOf(calendar.get(Calendar.YEAR)));
        lblEdadAdminLab.setText(String.valueOf(2024 - calendar.get(Calendar.YEAR)));
    }

